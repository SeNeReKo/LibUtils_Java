package de.general.util;


import java.util.*;


/**
 *
 * @author knauth
 */
public class StringFactory
{

	////////////////////////////////////////////////////////////////
	// Constants
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Variables
	////////////////////////////////////////////////////////////////

	final HashMap<String, String> map;

	////////////////////////////////////////////////////////////////
	// Constructors
	////////////////////////////////////////////////////////////////

	public StringFactory()
	{
		map = new HashMap<String, String>();
	}

	////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////

	public synchronized String get(String s)
	{
		String s2 = map.get(s);
		if (s2 != null) return s2;
		map.put(s, s);
		return s;
	}

}
