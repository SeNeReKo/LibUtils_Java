package de.general.util;


import java.util.*;


/**
 * This calss implements a list of integer values.
 */
public class IntList extends ArrayList<Integer>
{

	////////////////////////////////////////////////////////////////
	// Constants
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Variables
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Constructors
	////////////////////////////////////////////////////////////////

	public IntList()
	{
	}

	public IntList(int[] data)
	{
		if (data != null) {
			for (int v : data) {
				add(v);
			}
		}
	}

	////////////////////////////////////////////////////////////////
	// Properties
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////

}
