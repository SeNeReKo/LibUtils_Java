package de.general.util;


import java.lang.reflect.*;



/**
 *
 * @author knauth
 */
public class GenericArrayFactory<E>
{

	////////////////////////////////////////////////////////////////
	// Constants
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Variables
	////////////////////////////////////////////////////////////////

	private Class<E> eclass;

	////////////////////////////////////////////////////////////////
	// Constructors
	////////////////////////////////////////////////////////////////

	public GenericArrayFactory(Class<E> eclass)
	{
		try {
			this.eclass = eclass;
		} catch (Exception ee) {
			throw new RuntimeException(ee.getMessage(), ee);
		}
	}

	////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////

	public E[] createArray(int length)
	{
		Object[] ret = (Object[])(java.lang.reflect.Array.newInstance(eclass, length));
		return (E[])(ret);
	}

}
