package de.general.util;


import java.util.*;



/**
 *
 * @author knauth
 */
public class StringSet
{

	////////////////////////////////////////////////////////////////
	// Constants
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Variables
	////////////////////////////////////////////////////////////////

	HashMap<String, String> data;

	////////////////////////////////////////////////////////////////
	// Constructors
	////////////////////////////////////////////////////////////////

	public StringSet()
	{
		data = new HashMap<String, String>();
	}

	////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////

	public String s(String s)
	{
		if (s == null) return null;
		String s2 = data.get(s);
		if (s2 == null) {
			data.put(s, s);
			return s;
		} else {
			return s2;
		}
	}

}
