package de.general.util;


/**
 *
 * @author knauth
 */
public class Counter
{

	////////////////////////////////////////////////////////////////
	// Constants
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Variables
	////////////////////////////////////////////////////////////////

	int n;

	////////////////////////////////////////////////////////////////
	// Constructors
	////////////////////////////////////////////////////////////////

	public Counter()
	{
	}

	////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////

	public void increment()
	{
		n++;
	}

	public int getValue()
	{
		return n;
	}

	@Override
	public String toString()
	{
		return "" + n;
	}

}
