package de.general.util;


/**
 * A convenience class to implement String-Object-key-value-pairs
 *
 * @author knauth
 */
public class KVP
{

	////////////////////////////////////////////////////////////////
	// Constants
	////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////
	// Variables
	////////////////////////////////////////////////////////////////

	String key;
	Object value;

	////////////////////////////////////////////////////////////////
	// Constructors
	////////////////////////////////////////////////////////////////

	/**
	 * Constructor.
	 */
	public KVP(String key, Object value)
	{
		this.key = key;
		this.value = value;
	}

	////////////////////////////////////////////////////////////////
	// Methods
	////////////////////////////////////////////////////////////////

	public String getKey()
	{
		return key;
	}

	public Object getValue()
	{
		return value;
	}

}
